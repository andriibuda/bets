/**
 * Simple function to print associative list
 *
 * @param $array
 */
function f1($array) {
    foreach ($array as $delta => $item) {
        echo $delta . '|' . $item . "<br>";
    }
}

/**
 * Delete Count Number with ()
 *
 * @param $array
 */
function f2($array) {
    foreach ($array as $delta => $item) {
        $item = preg_replace('/\(.*\)/', '', $item);
        echo $delta . '|' . $item . "<br>";
    }
}

/**
 * Creates list with lowercased values
 *
 * @param $array
 */
function f3($array) {
    foreach ($array as $delta => $item) {
        $item = str_replace(' ', '_', $item);
        $item = str_replace('-', '_', $item);
        $delta = strtolower($item);
        echo $delta . '|' . $item . "<br>";
    }
}